#include "ATmega32.h"
#include "DIO.h"
#include "WDT.h"

#define F_CPU 1000000UL
#include "util/delay.h"
//========================================================================


//========================================================================

//=========================================================================

int main(void)
{
	DIO_PIN_DIRECTION(DIOC,PIN1,OUTPUT);
	WDT_EN(WDT_2048K_PRESCALER);
	
	while(1)
	{
		
		DIO_WRITE_PIN(DIOC,PIN1,HIGH);
		_delay_ms(1000);
		DIO_WRITE_PIN(DIOC,PIN1,LOW);
		_delay_ms(1000);
		while(1);
	}
	
	return 0;
}

